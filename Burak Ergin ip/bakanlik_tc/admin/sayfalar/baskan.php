			<p>1979 yılında Mersin’de doğdu. Konya Çumra Ziraat Meslek Lisesini bitirdikten sonra lisans eğitimini Selçuk Üniversitesi Ziraat Fakültesi Tarım Makinaları bölümünde tamamladı. “Konya İli Karapınar İlçesi Küçükbaş Hayvancılık İşletmelerinde Çalışan Kadınların Bilgiye Ulaşım Düzeyleri ve Yayım Yaklaşımları” konulu tezle mezun olduğu Selçuk Üniversitesi Ziraat Fakültesi Tarım Ekonomisi bölümünde yüksek lisansını yaptı.</p>


<p>Anadolu Üniversitesi Açıköğretim Fakültesi Radyo TV Programcılığı bölümünde ön lisansını tamamladı. “Web Tasarımı - Bilgisayar Teknolojisinin Tarımda Kullanımı - Tarım Yayımcılığı - Diksiyon, Spikerlik, Sunuculuk - Sosyal Medya Uzmanlığı - Dijital Pazarlama Uzmanlığı” gibi birçok konuda eğitim aldı. Anadolu Üniversitesi Açıköğretim Fakültesi Halkla İlişkiler ve Reklamcılık bölümünde lisans eğitimini tamamladı. </p>


<p>1998 yılında Tarım ve Orman Bakanlığında göreve başlayarak Niğde, Karaman, Şırnak ve Konya’daki İl ve İlçe Tarım ve Orman Müdürlükleri bünyesinde Ziraat Teknisyeni, Ziraat Yüksek Mühendisi, Çiftçi Eğitim ve Yayım Şubesi Müdür Vekili unvanlarıyla hizmette bulundu. Şırnak'ta görev yaparken aynı zamanda TRT'nin yerel muhabirliğini de yaptı. 2009’dan itibaren ise meslek hayatını Ankara’da Tarım ve Orman Bakanlığı Eğitim ve Yayın Dairesi Başkanlığında sürdürdü. Bu kurumda Çalışma Grup Sorumlusu, Uluslararası Tarımsal Eğitim Merkezi Müdür Vekilliği ve Daire Başkanı olarak etkin faaliyetlerde bulundu.</p>


<p>Eğitim ve Yayın Dairesi Başkanlığında proje koordinatörlüğünden sunuculuğa, program asistanlığından seslendirmeye kadar her alanda başarılı projelere imza attı. Web Tarım Tv Projesi’nin hayata geçirilmesi ve yürütülmesinde etkin rol almakla kalmadı, tarımla ilgili internet yayıncılığında birçok kamu kurumuna da rehberlik etti.</p>


